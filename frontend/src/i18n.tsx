export type Dict = (typeof DICT)['ar']

interface LangCtx {
  lang: Lang
  dir: 'rtl' | 'ltr'
  t: Dict
  setLang: (l: Lang) => void
  /** pick the language-appropriate name from a {name_ar, name_en} object */
  name: (o: { name_ar: string; name_en: string }) => string
  altName: (o: { name_ar: string; name_en: string }) => string
}

const Ctx = createContext<LangCtx | null>(null)

export function LangProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>(() => {
    // ?lang=ar|en deep-links a demo state (screenshots, shared links)
    const q = new URLSearchParams(window.location.search).get('lang')
    const initial = q === 'ar' || q === 'en' ? q : (localStorage.getItem('delta-lang') as Lang) || 'ar'
    setFormatLang(initial) // sync, before first render formats anything
    return initial
  })

  const setLang = (l: Lang) => {
    setFormatLang(l) // sync, so the re-render formats in the new locale
    setLangState(l)
  }

  useEffect(() => {
    document.documentElement.lang = lang
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr'
    localStorage.setItem('delta-lang', lang)
  }, [lang])

  const value = useMemo<LangCtx>(
    () => ({
      lang,
      dir: lang === 'ar' ? 'rtl' : 'ltr',
      t: DICT[lang],
      setLang,
      name: (o) => (lang === 'ar' ? o.name_ar : o.name_en),
      altName: (o) => (lang === 'ar' ? o.name_en : o.name_ar),
    }),
    [lang],
  )

  return <Ctx.Provider value={value}>{children}</Ctx.Provider>
}

export function useLang(): LangCtx {
  const ctx = useContext(Ctx)
  if (!ctx) throw new Error('useLang outside LangProvider')
  return ctx
}
